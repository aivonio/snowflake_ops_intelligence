-- 5. Create Application Object
CREATE APPLICATION IF NOT EXISTS SNOWFLAKE_OPS_APP
  FROM APPLICATION PACKAGE SNOWFLAKE_OPS_PACKAGE
  USING '@SNOWFLAKE_OPS_PACKAGE.STAGE_CONTENT.APP_STAGE';

-- 6. Grant Required Privileges (CRITICAL for Advanced Features)
-- 
-- "IMPORTED PRIVILEGES" on the SNOWFLAKE database is the KEY permission.
GRANT IMPORTED PRIVILEGES ON DATABASE SNOWFLAKE TO APPLICATION SNOWFLAKE_OPS_APP;

-- Allow app to access its own storage database
GRANT USAGE ON DATABASE SNOWFLAKE_OPS_APP_DATA TO APPLICATION SNOWFLAKE_OPS_APP;
GRANT USAGE ON ALL SCHEMAS IN DATABASE SNOWFLAKE_OPS_APP_DATA TO APPLICATION SNOWFLAKE_OPS_APP;
GRANT SELECT, INSERT, UPDATE, DELETE ON ALL TABLES IN DATABASE SNOWFLAKE_OPS_APP_DATA TO APPLICATION SNOWFLAKE_OPS_APP;

-- Allow app to use Cortex (for AI Analyst & AI Optimizer)
GRANT DATABASE ROLE SNOWFLAKE.CORTEX_USER TO APPLICATION SNOWFLAKE_OPS_APP;

-- Allow app to use Warehouse
GRANT USAGE ON WAREHOUSE SNOWFLAKE_OPS_APP_WH TO APPLICATION SNOWFLAKE_OPS_APP;

-- 7. Create Roles for Users (RBAC)
-- Create a Power User role that has access to Cost/Usage data WITHOUT being AccountAdmin
CREATE ROLE IF NOT EXISTS SNOWFLAKE_OPS_ADMIN;
CREATE ROLE IF NOT EXISTS SNOWFLAKE_OPS_USER;

-- Grant permissions to the Admin Role
GRANT DATABASE ROLE SNOWFLAKE.GOVERNANCE_VIEWER TO ROLE SNOWFLAKE_OPS_ADMIN;
GRANT DATABASE ROLE SNOWFLAKE.USAGE_VIEWER TO ROLE SNOWFLAKE_OPS_ADMIN;

-- Grant App Access to the Roles
GRANT USAGE ON APPLICATION SNOWFLAKE_OPS_APP TO ROLE SNOWFLAKE_OPS_ADMIN;
GRANT USAGE ON APPLICATION SNOWFLAKE_OPS_APP TO ROLE SNOWFLAKE_OPS_USER;
GRANT USAGE ON WAREHOUSE SNOWFLAKE_OPS_APP_WH TO ROLE SNOWFLAKE_OPS_ADMIN;
GRANT USAGE ON WAREHOUSE SNOWFLAKE_OPS_APP_WH TO ROLE SNOWFLAKE_OPS_USER;

