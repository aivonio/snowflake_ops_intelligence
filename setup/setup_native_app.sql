-- USE THIS SCRIPT for Internal Hosting (Native App Mode).
-- It sets up the Database, Warehouse, Application, and ALL required Security/AI permissions.
--
-- DO NOT RUN 'setup.sql' or 'setup_dummy.sql' if you are using this one.
--
-- RUN AS: ACCOUNTADMIN
