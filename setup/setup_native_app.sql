USE ROLE ACCOUNTADMIN;

-- 0. Set up Warehouse
CREATE WAREHOUSE IF NOT EXISTS SNOWFLAKE_OPS_APP_WH 
  WAREHOUSE_SIZE = XSMALL 
  AUTO_SUSPEND = 60 
  AUTO_RESUME = TRUE;
USE WAREHOUSE SNOWFLAKE_OPS_APP_WH;

-- 1. Create Application Package
CREATE APPLICATION PACKAGE IF NOT EXISTS SNOWFLAKE_OPS_PACKAGE;

-- 2. Create Schema for Stage (CRITICAL: Stage needs a schema inside the package)
CREATE SCHEMA IF NOT EXISTS SNOWFLAKE_OPS_PACKAGE.STAGE_CONTENT;

-- 3. Create Data Structure for persistence
-- This database will store your app settings, budgets, and cached metrics
CREATE DATABASE IF NOT EXISTS SNOWFLAKE_OPS_APP_DATA;
CREATE SCHEMA IF NOT EXISTS SNOWFLAKE_OPS_APP_DATA.APP_CONTEXT;
CREATE SCHEMA IF NOT EXISTS SNOWFLAKE_OPS_APP_DATA.APP_ANALYTICS;

-- Table DDL for persistence
CREATE TABLE IF NOT EXISTS SNOWFLAKE_OPS_APP_DATA.APP_CONTEXT.PLATFORM_SETTINGS (
    SETTING_KEY VARCHAR PRIMARY KEY,
    SETTING_VALUE VARCHAR,
    DESCRIPTION VARCHAR,
    UPDATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

CREATE TABLE IF NOT EXISTS SNOWFLAKE_OPS_APP_DATA.APP_CONTEXT.BUDGET_ALERTS (
    ALERT_ID NUMBER AUTOINCREMENT PRIMARY KEY,
    ALERT_NAME VARCHAR,
    ALERT_TYPE VARCHAR,
    TARGET_NAME VARCHAR,
    THRESHOLD_CREDITS FLOAT,
    THRESHOLD_PERCENTAGE FLOAT,
    NOTIFICATION_CHANNEL VARCHAR DEFAULT 'DASHBOARD',
    IS_ACTIVE BOOLEAN DEFAULT TRUE
);

CREATE TABLE IF NOT EXISTS SNOWFLAKE_OPS_APP_DATA.APP_ANALYTICS.METADATA_CACHE (
    CACHE_KEY VARCHAR PRIMARY KEY,
    CACHE_VALUE VARIANT,
    EXPIRY_TIME TIMESTAMP_NTZ
);

CREATE TABLE IF NOT EXISTS SNOWFLAKE_OPS_APP_DATA.APP_CONTEXT.WAREHOUSE_CONTEXT (
    WAREHOUSE_NAME VARCHAR PRIMARY KEY,
    PURPOSE VARCHAR DEFAULT 'GENERAL',
    SIZE VARCHAR,
    COST_PROFILE VARCHAR DEFAULT 'BALANCED',
    CONCURRENCY_TOLERANCE VARCHAR DEFAULT 'MEDIUM',
    OWNER_TEAM VARCHAR,
    NOTES VARCHAR,
    UPDATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

CREATE TABLE IF NOT EXISTS SNOWFLAKE_OPS_APP_DATA.APP_CONTEXT.TABLE_CONTEXT (
    DATABASE_NAME VARCHAR,
    SCHEMA_NAME VARCHAR,
    TABLE_NAME VARCHAR,
    FRESHNESS_REQUIREMENT VARCHAR DEFAULT 'DAILY',
    ACCESS_FREQUENCY VARCHAR DEFAULT 'UNKNOWN',
    IS_CRITICAL BOOLEAN DEFAULT FALSE,
    PRIMARY KEY (DATABASE_NAME, SCHEMA_NAME, TABLE_NAME)
);

CREATE TABLE IF NOT EXISTS SNOWFLAKE_OPS_APP_DATA.APP_CONTEXT.TEAM_ATTRIBUTION (
    USER_NAME VARCHAR PRIMARY KEY,
    TEAM_NAME VARCHAR,
    DEPARTMENT VARCHAR,
    COST_CENTER VARCHAR,
    BUDGET_LIMIT_CREDITS FLOAT,
    UPDATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

CREATE TABLE IF NOT EXISTS SNOWFLAKE_OPS_APP_DATA.APP_ANALYTICS.QUERY_BENCHMARK (
    BENCHMARK_ID NUMBER AUTOINCREMENT PRIMARY KEY,
    QUERY_TEXT VARCHAR,
    QUERY_HASH VARCHAR,
    RUN_TYPE VARCHAR, -- 'BASELINE' or 'OPTIMIZED'
    PREDICTED_COST_CREDITS FLOAT,
    ACTUAL_COST_CREDITS FLOAT,
    PREDICTED_TIME_MS NUMBER,
    ACTUAL_TIME_MS NUMBER,
    BYTES_SCANNED NUMBER,
    WAREHOUSE_USED VARCHAR,
    WAREHOUSE_SIZE VARCHAR,
    OPTIMIZATION_APPLIED VARCHAR,
    COST_SAVINGS_CREDITS FLOAT,
    TIME_SAVINGS_MS NUMBER,
    RUN_TIMESTAMP TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- 4. Create Stage for App Files
CREATE OR REPLACE STAGE SNOWFLAKE_OPS_PACKAGE.STAGE_CONTENT.APP_STAGE 
  DIRECTORY = (ENABLE = TRUE)
  FILE_FORMAT = (TYPE = 'CSV' FIELD_DELIMITER = '|' SKIP_HEADER = 1);

